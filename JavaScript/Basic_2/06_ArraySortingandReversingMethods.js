// sort()
// reverse()


//sort method :- The sort() method modifies the original array.

// The sort() method returns the sorted array.

// The default sort order is according to the string Unicode code points.

// The sort() method can take a compare function as an argument to customize the sort order.

//  The sort() method is not stable, meaning that equal elements may not keep their original order.

const number = [4, 2, 7, 1, 3, 3, 1];
number.sort()
console.log(number)

//reverse():- The reverse() method modifies the original array.

// The reverse() method returns the reversed array.

// The reverse() method reverses the order of the elements in the array, but does not change the values of the elements.

number.reverse()
console.log(number)



